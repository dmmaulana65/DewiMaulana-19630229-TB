    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here
        
        String JK = "";
        if(rdL.isSelected()){
            JK = "Laki-laki";
        }else if(rdP.isSelected()){
            JK = "Perempuan";
        }else{
            JOptionPane.showMessageDialog(null, "Anda belum memilih jenis kelamin");
        }        
       
        
        if(txtNip.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "NIP tidak boleh kosong", "Data Kosong", JOptionPane.ERROR_MESSAGE);
        }else  if(txtNama.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Nama tidak boleh kosong", "Data Kosong", JOptionPane.ERROR_MESSAGE);
        }else if(txtTTL.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Tempat Tanggal Lahir tidak boleh kosong", "Data Kosong", JOptionPane.ERROR_MESSAGE);
        }else if(cbAgama.getSelectedItem().equals("--Pilih Agama--")){
            JOptionPane.showMessageDialog(null, "Anda belum memilih agama", "Data Kosong", JOptionPane.ERROR_MESSAGE);
        }else if(txtTelp.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "No Telephone tidak boleh kosong", "Data Kosong", JOptionPane.ERROR_MESSAGE);
        }else if(txtSk.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "No SK tidak boleh kosong", "Data Kosong", JOptionPane.ERROR_MESSAGE);
        }else if(txtIjazah.getText().isEmpty()){
             JOptionPane.showMessageDialog(null, "Ijazah tidak boleh kosong", "Data Kosong", JOptionPane.ERROR_MESSAGE);
        }else if(cbJabatan.getSelectedItem().equals("--Pilih Jabatan--")){
            JOptionPane.showMessageDialog(null, "Anda belum memilih jabatan", "Data Kosong", JOptionPane.ERROR_MESSAGE); 
      
        }else{
           new koneksi().simpanPegawai(txtNip.getText(), txtNama.getText(), JK,txtTTL.getText(), String.valueOf(cbAgama.getSelectedItem()),txtSk.getText(), txtIjazah.getText(), txtTelp.getText(), String.valueOf(cbJabatan.getSelectedItem()));
           JOptionPane.showMessageDialog(null, "Data berhasil di simpan"); 
                
           new koneksi().judul(tbPegawai, Judul);
           new koneksi().tampilTable(tbPegawai, Judul, SQL);
           kosong();
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

