    private void tbPegawaiMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbPegawaiMouseClicked
        // TODO add your handling code here:
        
        txtNip.setText(tbPegawai.getValueAt(tbPegawai.getSelectedRow(), 0).toString());
        txtNama.setText(tbPegawai.getValueAt(tbPegawai.getSelectedRow(), 1).toString());
        if ("Laki-Laki".equals(tbPegawai.getValueAt(tbPegawai.getSelectedRow(), 2).toString())) {
        rdL.setSelected(true);
        } else  {
        rdP.setSelected(true);
        }
        txtTTL.setText(tbPegawai.getValueAt(tbPegawai.getSelectedRow(), 3).toString());
        cbAgama.setSelectedItem(tbPegawai.getValueAt(tbPegawai.getSelectedRow(), 4).toString());
        txtTelp.setText(tbPegawai.getValueAt(tbPegawai.getSelectedRow(), 5).toString());
        txtSk.setText(tbPegawai.getValueAt(tbPegawai.getSelectedRow(), 6).toString());
        txtIjazah.setText(tbPegawai.getValueAt(tbPegawai.getSelectedRow(), 7).toString());
        cbJabatan.setSelectedItem(tbPegawai.getValueAt(tbPegawai.getSelectedRow(), 8).toString());
        txtNip.setEditable(false);
        btnSimpan.setText("Ubah");
    
    }//GEN-LAST:event_tbPegawaiMouseClicked

